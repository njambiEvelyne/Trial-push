# Iterating over lists
fruits = ["banana", "Oranges", "Apples"]
for fruit in fruits:
  print(fruit)
  print(len(fruit))

# Working over the Tuples
colors = ("red", "green", "blue")
for color in colors:
  print(color)

# Iterating through the characters in a string
message = "Hello world!"  
for character in message:
  print(f"The character is :{character}")

# Iterating over range
for number in range(1,6)  :
  print(f"The numbers are: {number}")